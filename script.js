document.addEventListener("DOMContentLoaded", () => {
    // Force visibility
    document.body.style.opacity = "1";
    document.body.style.visibility = "visible";

    let currentLang = localStorage.getItem('dental_lang') || 'en';

    const updateLanguage = (lang) => {
        currentLang = lang;
        localStorage.setItem('dental_lang', lang);
        document.documentElement.lang = lang;
        document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
        
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                const content = translations[lang][key];
                if (content.includes('<br>')) {
                    el.innerHTML = content;
                } else {
                    el.textContent = content;
                }
            }
        });

        const langBtn = document.getElementById('lang-btn');
        if (langBtn) langBtn.textContent = lang === 'en' ? 'العربية' : 'English';
    };

    updateLanguage(currentLang);

    const langBtn = document.getElementById('lang-btn');
    if (langBtn) {
        langBtn.addEventListener('click', () => {
            updateLanguage(currentLang === 'en' ? 'ar' : 'en');
        });
    }

    // Mobile Menu Toggle Logic
    const menuToggle = document.getElementById('menu-toggle');
    const navMenu = document.querySelector('.nav-menu');
    const navLinks = document.querySelectorAll('.nav-menu a');

    if (menuToggle && navMenu) {
        menuToggle.addEventListener('click', () => {
            navMenu.classList.toggle('active');
            const icon = menuToggle.querySelector('i');
            if (navMenu.classList.contains('active')) {
                icon.className = 'fa-solid fa-xmark';
            } else {
                icon.className = 'fa-solid fa-bars';
            }
        });

        // Close menu when a link is clicked
        navLinks.forEach(link => {
            link.addEventListener('click', () => {
                navMenu.classList.remove('active');
                const icon = menuToggle.querySelector('i');
                icon.className = 'fa-solid fa-bars';
            });
        });
    }

    // Basic Slider
    const slides = document.querySelectorAll('.hero-slider .slide');
    if (slides.length > 0) {
        let currentSlide = 0;
        setInterval(() => {
            slides[currentSlide].classList.remove('active');
            currentSlide = (currentSlide + 1) % slides.length;
            slides[currentSlide].classList.add('active');
        }, 5000);
    }

    // Modal Opening Logic
    const modal = document.getElementById('booking-modal');
    const closeBtn = document.querySelector('.modal-close');
    const serviceSelect = document.getElementById('service-select');
    const bookingButtons = document.querySelectorAll('.service-btn, .btn-primary, .book-btn, [data-i18n="btn-book"], .btn-submit');

    if (modal) {
        bookingButtons.forEach(btn => {
            btn.addEventListener('click', (e) => {
                const href = btn.getAttribute('href');
                if (href && href.startsWith('#') && href !== '#contact') return;

                e.preventDefault();
                modal.classList.add('active');
                document.body.style.overflow = 'hidden';
            });
        });

        if (closeBtn) {
            closeBtn.addEventListener('click', () => {
                modal.classList.remove('active');
                document.body.style.overflow = '';
            });
        }
    }

    // GSAP ANIMATIONS
    if (typeof gsap !== 'undefined' && typeof ScrollTrigger !== 'undefined') {
        gsap.registerPlugin(ScrollTrigger);

        // General Fade Up
        document.querySelectorAll('.fade-up').forEach(el => {
            gsap.from(el, {
                y: 50, opacity: 0, duration: 1, ease: "power3.out",
                scrollTrigger: { trigger: el, start: "top 85%" }
            });
        });

        // General Fade Down
        document.querySelectorAll('.fade-down').forEach(el => {
            gsap.from(el, {
                y: -60, opacity: 0, duration: 1, ease: "power3.out",
                scrollTrigger: { trigger: el, start: "top 85%" }
            });
        });


        // Transformation Section
        gsap.from('.transformation-image-box', {
            x: -100, opacity: 0, duration: 1.2, ease: "expo.out",
            scrollTrigger: { trigger: '.transformation', start: "top 70%" }
        });
        gsap.from('.transformation-content', {
            x: 100, opacity: 0, duration: 1.2, ease: "expo.out",
            scrollTrigger: { trigger: '.transformation', start: "top 70%" }
        });

        // Appointment Wrapper
        gsap.from('.appointment-wrapper', {
            scale: 0.95, opacity: 0, duration: 1, ease: "back.out(1.7)",
            scrollTrigger: { trigger: '.appointment', start: "top 75%" }
        });
    }

    // Review Swiper Logic
    if (typeof Swiper !== 'undefined') {
        const reviewsSwiper = new Swiper('.reviews-slider-wrapper', {
            slidesPerView: 1,
            spaceBetween: 30,
            loop: true,
            autoplay: {
                delay: 4000,
                disableOnInteraction: false,
            },
            pagination: {
                el: '.swiper-pagination',
                clickable: true,
            },
            breakpoints: {
                768: {
                    slidesPerView: 2,
                },
                1024: {
                    slidesPerView: 3,
                }
            }
        });
    }

    // Booking Form Submission
    const bookingForm = document.getElementById('main-booking-form');
    if (bookingForm) {
        bookingForm.addEventListener('submit', (e) => {
            e.preventDefault();
            const btn = bookingForm.querySelector('button[type="submit"]');
            const originalText = btn.textContent;
            btn.textContent = translations[currentLang]["form-sent"];
            btn.style.backgroundColor = "#22c55e";
            bookingForm.reset();
            setTimeout(() => {
                btn.textContent = originalText;
                btn.style.backgroundColor = "";
            }, 3000);
        });
    }

    // LUXURY COUNTER ANIMATION
    const initCounters = () => {
        document.querySelectorAll('.counter').forEach(counter => {
            const target = +counter.getAttribute('data-target');
            if (!target) return;

            const update = () => {
                const step = target / 50;
                let current = 0;
                const timer = setInterval(() => {
                    current += step;
                    if (current >= target) {
                        counter.innerText = target.toLocaleString();
                        clearInterval(timer);
                    } else {
                        counter.innerText = Math.floor(current).toLocaleString();
                    }
                }, 30);
            };

            if (typeof gsap !== 'undefined' && typeof ScrollTrigger !== 'undefined') {
                ScrollTrigger.create({
                    trigger: counter,
                    start: "top 90%",
                    once: true,
                    onEnter: update
                });
            } else {
                update();
            }
        });
    };
    initCounters();

    // Discount Popup Logic
    const discountModal = document.getElementById('discount-modal');
    const discountClose = document.querySelector('.discount-modal-close');
    const discountClaimBtn = document.querySelector('.discount-claim-btn');

    const showDiscountPopup = () => {
        const hasSeenPopup = localStorage.getItem('sliage_clinic_discount_seen_v2');
        if (!hasSeenPopup) {
            setTimeout(() => {
                if (discountModal) {
                    discountModal.classList.add('active');
                    localStorage.setItem('sliage_clinic_discount_seen_v2', 'true');
                }
            }, 3000); // 3 second delay
        }
    };

    if (discountModal) {
        // Show popup
        showDiscountPopup();

        // Close on X click
        if (discountClose) {
            discountClose.addEventListener('click', () => {
                discountModal.classList.remove('active');
            });
        }

        // Close on clicking outside the content
        discountModal.addEventListener('click', (e) => {
            if (e.target === discountModal) {
                discountModal.classList.remove('active');
            }
        });

        // Claim button logic
        if (discountClaimBtn) {
            discountClaimBtn.addEventListener('click', (e) => {
                e.preventDefault();
                discountModal.classList.remove('active');
                
                // Open the main booking modal
                const mainBookingModal = document.getElementById('booking-modal');
                if (mainBookingModal) {
                    setTimeout(() => {
                        mainBookingModal.classList.add('active');
                        document.body.style.overflow = 'hidden';
                    }, 400); // Small delay for smooth transition
                }
            });
        }
    }
});
